#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = ft_strlen(s);
	while (i >= 0 && (s[i] != c))
		i--;
	if (s[i] == (char)c && i >= 0)
		return ((char *)s + i);
	return (NULL);
}
